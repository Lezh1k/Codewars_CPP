std::vector<std::string> split_lines(const std::string &str,
                                     const char separator /*= '\n'*/) {
  std::vector<std::string> res;
  size_t b = 0, e;
  do {
    e = str.find_first_of(separator, b);
    res.push_back(str.substr(b, e - b));
    b = e + 1;
  } while (e != std::string::npos);
  return res;
}
